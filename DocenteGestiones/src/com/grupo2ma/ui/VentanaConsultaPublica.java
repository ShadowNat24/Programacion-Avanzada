    public void refrescarTabla() {
        modelo.setRowCount(0);
        List<Licencia> lista = gestor.obtenerLicencias();
        for (Licencia l : lista) {
            modelo.addRow(new Object[]{
                    l.getDocente().getNombre(),
                    l.getDocente().getTurno(),
                    l.getDocente().getGrupo(),
                    l.getFechaInicio().toString(),
                    l.getFechaFin().toString(),
                    l.getTipo()
            });
        } }
            
